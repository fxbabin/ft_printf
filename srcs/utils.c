#include "ft_printf.h"

void	ft_buff_init(t_buff *b, int fd)
{
	ft_bzero(b->buff, BUFF_SIZE + 1);
	b->len = 0;
	b->pos = 0;
	b->err = 0;
	b->fd = fd;
	b->err_len = 0;
}
