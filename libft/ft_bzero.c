#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while ((int)(i * 8) <= (int)(n - 8))
	{
		*((int64_t *)s + i) = 0;
		i++;
	}
	i *= 2;
	if ((int)(i * 4) <= (int)(n - 4))
	{
		*((int *)s + i) = 0;
		i++;
	}
	i *= 2;
	if ((int)(i * 2) <= (int)(n - 2))
	{
		*((short *)s + i) = 0;
		i++;
	}
	i *= 2;
	if (i < n)
		*((char *)s + i) = 0;
}
